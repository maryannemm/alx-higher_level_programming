#!/usr/bin/node

class Rectangle {
    constructor(w, h) {
        /**
         * Initializes a Rectangle instance with given width and height.
         *
         * @param {number} w - Width of the rectangle.
         * @param {number} h - Height of the rectangle.
         */
        this.width = w;
        this.height = h;
    }
}

module.exports = Rectangle;

